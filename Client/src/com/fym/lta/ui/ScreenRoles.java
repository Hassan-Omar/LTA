            permission3+="update" ;
        if(viewPermissionMod3.isSelected())
            permission3+="view" ;
        if(deletePermissionMod3.isSelected())
            permission3+="delete" ;
        if(insertPermissionMod3.isSelected())
            permission3+="insert" ;
        
