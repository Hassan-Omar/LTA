            selectedScreens.add(new ScreenDto(13)); 
        
        if (ScheduleCheckbox.isSelected())
            selectedScreens.add(new ScreenDto(13)); 

       
        // permission on model 1
        if(updatePermissionMod1.isSelected())
            permission1+="update" ;
